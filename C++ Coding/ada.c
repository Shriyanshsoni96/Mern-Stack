#include <stdio.h>   // standard input-output library

int main() {
    // Print a message
    printf("Hello, World!\n");

    // Example: declaring variables
    int a = 10;
    float b = 5.5;
    char c = 'A';

    // Printing variables
    printf("Integer: %d\n", a);
    printf("Float: %.2f\n", b);
    printf("Character: %c\n", c);

    return 0;  // program ends
}
